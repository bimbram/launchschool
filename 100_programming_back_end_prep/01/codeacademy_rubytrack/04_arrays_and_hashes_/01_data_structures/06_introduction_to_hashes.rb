# Introduction to Hashes
#===============================================================================
#
# -Hashes are like JS objects, the difference with JS object is that a hash is using
# '=>' instead of ':' to seperate between its key and value
#
#===============================================================================
#
# Instruction
#
#===============================================================================
#
# 1. We've created a hash in the editor to the right. See how it's made up of keys
#    and values? Check out the code below it: it should look vaguely familiar. Click
#    Run to see what it'll do!
#
#===============================================================================
#
# script.rb
#
#===============================================================================

my_hash = { "name" => "Eric",
  "age" => 26,
  "hungry?" => true
}

puts my_hash["name"]
puts my_hash["age"]
puts my_hash["hungry?"]

