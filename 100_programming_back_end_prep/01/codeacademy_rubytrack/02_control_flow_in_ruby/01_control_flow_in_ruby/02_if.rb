# If
#===============================================================================
#
# -'if' statement takes an expression
# -if that expression is 'true' Ruby will execute the block of code that follows
# -if that expression is 'false' the block of code that follows will not be executed
# -whitespace: spaces and blank lines
# -Ruby does not care about whitespace, but there's a convention that the block of
# code following an 'if' should be idented two spaces
#
#===============================================================================
#
# Instruction
#
#===============================================================================
#
# 1. Write your own if statement in the editor. It can take any expression you want
#   (even just true!), but it should evaluate to true. When it does, it should print
#    a string of your choice to the console (using print or puts).
#
#===============================================================================
#
# script.rb
#
#===============================================================================

if true
  puts "test"
end

