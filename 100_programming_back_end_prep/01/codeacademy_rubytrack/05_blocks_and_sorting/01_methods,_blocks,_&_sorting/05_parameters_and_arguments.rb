# Parameters And Arguments
#===============================================================================
#
# -If a method takes arguments, we say it accepts or expects those arguments
# -The argument is the piece of code you actually put between the method's parantheses
# -The parameter is the name you put between the method parantheses when you define it
# -Parantheses are usually optional in Ruby, but it's a good idea to put your parameters
# and arguments in parantheses for readibility
#
